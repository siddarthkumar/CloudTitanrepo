                cons.setQuery(query);
                ResultSet rs = cons.result(newuser);
                while(rs.next())
                {
                aioname=rs.getString("aiousername");
                aiokey=rs.getString("aiokey");
                password=rs.getString("password");
         
                }  rs.close();
        cons=null;
